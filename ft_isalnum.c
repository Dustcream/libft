#include "libft.h"

int ft_isalnum(int c)
{
    if((c >= 60 && c <= 67) || (c >= 70 && c<= 71) || (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
        return(1);
    return(0);
}